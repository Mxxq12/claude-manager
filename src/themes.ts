export function getTheme(name: string): Theme {
  return themes.find((t) => t.name === name) || themes[0];
}

export function applyTheme(theme: Theme) {
  const root = document.documentElement;
  const c = theme.colors;
  root.style.setProperty('--bg', c.bg);
  root.style.setProperty('--bg-sidebar', c.bgSidebar);
  root.style.setProperty('--bg-toolbar', c.bgToolbar);
  root.style.setProperty('--text', c.text);
  root.style.setProperty('--text-muted', c.textMuted);
  root.style.setProperty('--text-dim', c.textDim);
  root.style.setProperty('--border', c.border);
  root.style.setProperty('--hover', c.hover);
  root.style.setProperty('--active', c.active);
  root.style.setProperty('--accent', c.accent);
  root.style.setProperty('--accent-hover', c.accentHover);
  root.style.setProperty('--success', c.success);
  root.style.setProperty('--error', c.error);
  root.style.setProperty('--selection', c.selection);
  localStorage.setItem('claude-manager-theme', theme.name);
}

export function loadSavedTheme(): Theme {
  const saved = localStorage.getItem('claude-manager-theme');
  return getTheme(saved || 'ayu-dark');
}
